#include <iostream>
using std::cout;
using std::endl;

int main(void)
{
	cout << "sizeof of bool:" << sizeof(bool) << endl;
	cout << "sizeof of char:" << sizeof(char) << endl;
	cout << "sizeof of wchar_t:" << sizeof(wchar_t) << endl;
	cout << "sizeof of char16_t:" << sizeof(char16_t) << endl;
	cout << "sizeof of char32_t:" << sizeof(char32_t) << endl;
	cout << "sizeof of short:" << sizeof(short) << endl;
	cout << "sizeof of int:" << sizeof(int) << endl;
	cout << "sizeof of long:" << sizeof(long) << endl;
	cout << "sizeof of long long:" << sizeof(long long) << endl;
	cout << "sizeof of float:" << sizeof(float) << endl;
	cout << "sizeof of double:" << sizeof(double) << endl;
	cout << "sizeof of long double:" << sizeof(long double) << endl;
	return 0;
}

